class Attention(nn.Module):

    def __init__(self, name, **params):
        super().__init__()
        attentions = {
            "scse":SCSEModule,
            "ppsa":ParallelPolarizedSelfAttention,
            "spsa":SequentialPolarizedSelfAttention
        }

        if name in attentions.keys():
            self.attention = attentions["name"](**params)
        else :
            self.attention = nn.Identity(**params)
            if name is not None:
                raise ValueError("Attention {} is not implemented.".format(name))


    def forward(self, x):
        return self.attention(x)


class Flatten(nn.Module):
    def forward(self, x):
        return x.view(x.shape[0], -1)
